	public void renderDoors(MatrixStack matrices, VertexConsumer vertices, int light, int overlay, float leftDoor, float rightDoor) {
		for (int i = 0; i < 10; i++) {
			DoorLeft[2 * i].pivotZ = -0.8125F * leftDoor;
			DoorLeft[2 * i + 1].pivotZ = 0.8125F * leftDoor;
			DoorRight[2 * i].pivotZ = -0.8125F * rightDoor;
			DoorRight[2 * i + 1].pivotZ = 0.8125F * rightDoor;
		}
		for (int i = 0; i < 20; i++) {
			DoorLeft[i].render(matrices, vertices, light, overlay);
			DoorRight[i].render(matrices, vertices, light, overlay);
		}
	}

	public void renderDoorLabels(MatrixStack matrices, VertexConsumer vertices, int light, int overlay, float leftDoor, float rightDoor) {
		for (int i = 0; i < 10; i++) {
			DoorLeftL[2 * i].pivotZ = -0.8125F * leftDoor;
			DoorLeftL[2 * i + 1].pivotZ = 0.8125F * leftDoor;
			DoorRightL[2 * i].pivotZ = -0.8125F * rightDoor;
			DoorRightL[2 * i + 1].pivotZ = 0.8125F * rightDoor;
		}
		for (int i = 0; i < 20; i++) {
			DoorLeftL[i].render(matrices, vertices, light, overlay);
			DoorRightL[i].render(matrices, vertices, light, overlay);
			DoorLeftSideL[i].render(matrices, vertices, light, overlay);
			DoorRightSideL[i].render(matrices, vertices, light, overlay);
		}
	}

